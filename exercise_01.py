

user_input = int(input("Enter a grade from 0 to 100: "))

if user_input >= 90:
    print("Your grade is an A")
elif user_input >= 80:
    print("Your grade is a B")
elif user_input >= 70:
    print("Your grade is a C")
elif user_input >= 60:
    print("Your grade is a D")
else:
    print("Your grade is a F")
